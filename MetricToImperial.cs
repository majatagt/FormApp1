
class Conversion
{
    public static double KgToPounds(double weight)
    {
        return weight * 2.20462;
    }
    public static double PoundsToKg(double height)
    {
        return height / 2.54;
    }
    public static double InchesToCm(double inches)
    {
        return inches * 2.54;
    }
    public static double CmToInch(double centimeter)
    {
        return centimeter / 2.54;
    }
    public static double MlToOz(double Oz)
    {
        return Oz * 29.5735;
    }
    public static double OzToMl(double Ml)
    {
        return Ml / 29.57335;
    }
}


